class managespecialitysController extends specialitylistController {
	private $PAGESIZE=10;
	public function DeleteItem($ID)
	{
		$Language_fid=CurrentLanguageManager::getCurrentLanguageID();
		$DBAccessor=new dbaccess();
		$su=new sessionuser();
        $role_systemuser_fid=$su->getSystemUserID();
        $UserID=null;
        if(!$this->getAdminMode())
            $UserID=$role_systemuser_fid;
		$specialityEnt=new ocms_specialityEntity($DBAccessor);
		$specialityEnt->setId($ID);
		if($specialityEnt->getId()==-1)
			throw new DataNotFoundException();
		if($UserID!=null && $specialityEnt->getRole_systemuser_fid()!=$UserID)
			throw new DataNotFoundException();
		$specialityEnt->Remove();
		$DBAccessor->close_connection();
		return $this->load(-1);
	}
}
